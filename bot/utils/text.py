# -*- coding: utf-8 -*-
"""Утилиты для форматирования текста."""


def greeting_by_hour(hour: int) -> tuple[str, str, str]:
    """
    Возвращает (заголовок шапки, слово-приветствие, эмодзи-суффикс) по часу суток 0–23.

    Временная зона: МСК (UTC+3) — вычислять снаружи.

    Примеры:
      6  → ('🌅 Доброе утро!',  'Доброе утро',  '☀️')
      14 → ('☀️ Добрый день!',  'Добрый день',  '👋')
      19 → ('🌇 Добрый вечер!', 'Добрый вечер', '🌆')
      2  → ('🌙 Доброй ночи!',  'Доброй ночи',  '🌙')
    """
    if 5 <= hour <= 11:
        return "🌅 Доброе утро!", "Доброе утро", "☀️"
    if 12 <= hour <= 16:
        return "☀️ Добрый день!", "Добрый день", "👋"
    if 17 <= hour <= 22:
        return "🌇 Добрый вечер!", "Добрый вечер", "🌆"
    return "🌙 Доброй ночи!", "Доброй ночи", "🌙"


def pluralize_days(n: int) -> str:
    """
    Возвращает строку «N день/дня/дней» с правильным русским склонением.

    Примеры: 1 день, 2 дня, 5 дней, 11 дней, 21 день, 22 дня, 101 день.
    """
    if 11 <= n % 100 <= 20:
        word = "дней"
    elif n % 10 == 1:
        word = "день"
    elif 2 <= n % 10 <= 4:
        word = "дня"
    else:
        word = "дней"
    return f"{n} {word}"
